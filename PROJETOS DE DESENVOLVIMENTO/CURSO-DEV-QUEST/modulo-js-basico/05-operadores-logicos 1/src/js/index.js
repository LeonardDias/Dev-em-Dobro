/*
    == ---- igual ---- A==B ---- Verdadeiro se A for igual B 
    === --- idêntico - A===B --- Verdadeiro se A for idêntico a B
    !== --- não identico - A!==B - Verdadeiro se o A não for idêntico ao B
    != ---- diferente - A!=B - Verdadeiro se o A for diferente de B
    < ---- menor que ---- A<B ---- Verdadeiro quando A for menor que B
    <= -- menor ou igual que - A<=B - Verdadeiro quando A for menor ou igual a B
    > ---- maior que ---- A>B ---- Verdadeiro quando A for maior que B
    >= -- maior ou igual que - A>=B - Verdadeiro quando A for maior ou igual a B
*/

// IGUAL

/*
const a = 3
const b = 3
console.log(a==b)
*/

//IDÊNTICO

/*
const a = '3'
const b = "3"
console.log(a===b)
*/

// NÃO IDÊNTICO

/*
const a = '3'
const b = "3"
console.log(a!==b)
*/

// DIFERENTE

/*
const a = 3
const b = "3"
console.log(a!=b)
*/

// MENOR QUE

/*
const a = 3
const b = 4
console.log(a<b)
*/

// MENOR OU IGUAL QUE

/*
const a = 3
const b = 3
console.log(a<=b)
*/

// MAIOR QUE

/*
const a = 4
const b = 3
console.log(a>b)
*/

// MAIOR OU IGUAL QUE

/*
const a = 4
const b = 4
console.log(a>=b)
*/
