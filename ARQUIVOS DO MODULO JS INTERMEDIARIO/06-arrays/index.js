// Uma array em JavaScript é uma estrutura de dados que armazena múltiplos valores em uma única variável. Esses valores podem ser de qualquer tipo, como números, strings, objetos ou até mesmo outras arrays. Arrays são muito úteis para armazenar listas de itens, como nomes, números, ou objetos, e podem ser acessados por índices.

// Aqui declaramos as variáveis

let filmes = ["Vingadores", "Mulher Maravilha", "Batman"]
let avaliacoes = [1, 4.6, 5]

// Aqui mostramos as impressões

console.log(filmes[1]);
console.log(avaliacoes[2]);
console.log(filmes);
console.log(avaliacoes);

// Podemos também fazer um array com multiplos tipos

let descricaoFilme = ["Encontro Marcado", 5.0, true]

console.log(descricaoFilme);

// Exemplo do que acontece quando damos console.log em um campo não existente
console.log(descricaoFilme[5]);





